public class Relational_Operator {
    public static void main(String[] args) {
        System.out.println("Relational Operator :");
        int x=10;
        int y=20;
        System.out.println("x==y is :"+(x==y));
        System.out.println("x>y is :"+(x>y));
        System.out.println("x<y is :"+(x<y));
        System.out.println("x<=y is :"+(x<=y));
        System.out.println("x>=y is :"+(x>=y));
        System.out.println("x==y is :"+(x!=y));
    }
}
