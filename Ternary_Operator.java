public class Ternary_Operator {
    public static void main(String[] args) {
        System.out.println("Ternary Operator :");
        int x=10;
        int y=20;
        int small=(x<y)?x:y;
        System.out.println("Small no. : "+small);

        // Second Example------

        int a=50;
        int b=100;
        int max=(b>a)?b:a;
        System.out.println("max no :"+max);
    }
}
